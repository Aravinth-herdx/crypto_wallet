class TransactionHistory {
  final String id;
  final String hash;
  final String from;
  final String to;
  final double amount;
  final String token;
  final String network;
  final double fee;
  final DateTime timestamp;
  final TransactionStatus status;
  final TransactionType type;

  TransactionHistory({
    required this.id,
    required this.hash,
    required this.from,
    required this.to,
    required this.amount,
    required this.token,
    required this.network,
    required this.fee,
    required this.timestamp,
    required this.status,
    required this.type,
  });
}

